//산술 연산자

/*

-산술 연산자는 2개의 피연산자가 필요.
+,- 부호로 사용을 해서 피연산자 하나만 부호를 바꿀수있다.

+양수
-음수

+ 덧셈
- 뺄셈
* 곱셈
/ 나눗셈(일반 : 몫)
% 나눗셈(나머지값을 들고옴)

*/


#include<stdio.h>

void main() {

	int a = 2, b = 3, c = 4;

	printf("%d\n",- a);
	printf("%d\n", a / b);
	printf("%d\n", a % b);
	printf("%d\n", a + b * c);
	printf("\n");


	// 두 정수를 입력하세요
	// 합은 : 
	// 차는 : 
	// 곱은 :
	// 몫은 :
	// 나머지는 : 

}